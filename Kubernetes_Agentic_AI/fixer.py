"""
Fixer Module - Execute LLM-generated Fixes

Executes any kubectl command or YAML patch generated by the LLM analyzer.
Handles:
- Direct kubectl commands
- YAML file modifications
- Deployment updates
- Pod deletions and recreate
"""

import subprocess
import re
import yaml
import json
from typing import Dict, Any, Optional, List
from datetime import datetime

from config import NAMESPACE, YAML_FILE_PATH, POD_NAME


def run_cmd(cmd: str, timeout: int = 60) -> Dict[str, Any]:
    """
    Run shell command and return result.

    Returns:
        Dict with success, output, error
    """
    try:
        result = subprocess.run(
            cmd, shell=True, capture_output=True, text=True, timeout=timeout
        )
        return {
            "success": result.returncode == 0,
            "output": result.stdout,
            "error": result.stderr,
            "returncode": result.returncode
        }
    except subprocess.TimeoutExpired:
        return {
            "success": False,
            "output": "",
            "error": "Command timed out",
            "returncode": -1
        }
    except Exception as e:
        return {
            "success": False,
            "output": "",
            "error": str(e),
            "returncode": -1
        }


def parse_fix_command(command: str) -> str:
    """Clean and validate kubectl command."""
    command = command.strip()

    # Remove markdown code blocks
    command = re.sub(r'^```kubectl', '', command)
    command = re.sub(r'^```', '', command)
    command = re.sub(r'```$', '', command)

    return command.strip()


def execute_fix_command(command: str) -> Dict[str, Any]:
    """
    Execute a kubectl fix command.

    Args:
        command: The kubectl command to execute

    Returns:
        Dict with success, output, command
    """
    if not command or not command.strip():
        return {
            "success": False,
            "command": "",
            "output": "",
            "error": "Empty command"
        }

    # Parse and validate
    clean_cmd = parse_fix_command(command)

    if not clean_cmd.startswith("kubectl"):
        return {
            "success": False,
            "command": clean_cmd,
            "output": "",
            "error": "Command must start with kubectl"
        }

    print(f"\n>>> Executing: {clean_cmd}")

    # Execute
    result = run_cmd(clean_cmd, timeout=120)

    return {
        "success": result["success"],
        "command": clean_cmd,
        "output": result["output"],
        "error": result["error"],
        "returncode": result["returncode"]
    }


def apply_yaml_fix(yaml_content: str) -> Dict[str, Any]:
    """
    Apply YAML content as a fix.

    Args:
        yaml_content: YAML string to apply

    Returns:
        Dict with success and output
    """
    # Save to temp file
    import tempfile
    import os

    with tempfile.NamedTemporaryFile(mode='w', suffix='.yaml',
                                     delete=False) as f:
        f.write(yaml_content)
        temp_file = f.name

    try:
        # Apply the YAML
        result = run_cmd(f"kubectl apply -f {temp_file} -n {NAMESPACE}")

        return {
            "success": result["success"],
            "output": result["output"],
            "error": result["error"]
        }
    finally:
        # Cleanup
        try:
            os.unlink(temp_file)
        except:
            pass


def delete_and_recreate(pod_name: str = None) -> Dict[str, Any]:
    """
    Delete problematic pod and let deployment recreate it.

    Args:
        pod_name: Pod to delete

    Returns:
        Dict with success and output
    """
    pod = pod_name or POD_NAME

    if not pod:
        return {
            "success": False,
            "output": "",
            "error": "No pod name specified"
        }

    # Delete pod
    cmd = f"kubectl delete pod {pod} -n {NAMESPACE} --grace-period=0 --force"
    print(f"\n>>> Deleting pod: {pod}")

    result = run_cmd(cmd)

    return {
        "success": result["success"],
        "output": result["output"],
        "error": result["error"]
    }



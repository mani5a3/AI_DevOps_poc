def apply_fix_from_diagnosis(diagnosis_result) -> Dict[str, Any]:
    """
    Apply a fix based on diagnosis result.

    Args:
        diagnosis_result: DiagnosisResult from analyzer

    Returns:
        Dict with success, output, fix applied
    """
    fix_applied = False
    output = ""
    error = ""

    # Try the fix command first
    if diagnosis_result.fix_command:
        result = execute_fix_command(diagnosis_result.fix_command)
        if result["success"]:
            return {
                "success": True,
                "command": diagnosis_result.fix_command,
                "output": result["output"],
                "error": "",
                "fix_type": "command"
            }
        else:
            output = result.get("output", "")
            error = result.get("error", "")

    # Try YAML if command failed
    if diagnosis_result.fix_yaml:
        result = apply_yaml_fix(diagnosis_result.fix_yaml)
        if result["success"]:
            return {
                "success": True,
                "command": "",
                "output": result["output"],
                "error": "",
                "fix_type": "yaml"
            }

    # Last resort: delete and recreate
    if POD_NAME:
        result = delete_and_recreate(POD_NAME)
        return {
            "success": result["success"],
            "command": f"kubectl delete pod {POD_NAME}",
            "output": result["output"],
            "error": result["error"],
            "fix_type": "delete_recreate"
        }

    return {
        "success": False,
        "command": diagnosis_result.fix_command or "",
        "output": output,
        "error": error or "No fix available",
        "fix_type": "none"
    }


def retry_with_backoff(attempt: int, max_retries: int = 5) -> int:
    """
    Calculate backoff delay for retry.

    Args:
        attempt: Current attempt number (0-indexed)
        max_retries: Maximum retries

    Returns:
        Seconds to wait
    """
    import random

    if attempt >= max_retries - 1:
        return 0

    # Exponential backoff with jitter
    delay = min(2 ** attempt * 2, 60)  # Max 60 seconds
    jitter = random.uniform(0, 5)

    return int(delay + jitter)


